import mongoose from 'mongoose';

//creating user schema for user admin
const registrySchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    link: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Registry = mongoose.model('Registry', registrySchema);

export default Registry;
